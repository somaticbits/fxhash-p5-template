function render() {
  gl.viewport(0, 0, canvas.width, canvas.height)
  gl.clearColor(1, 1, 1, 1)
  gl.clear(gl.COLOR_BUFFER_BIT)

  // program.uTime = gl.getUniformLocation(program, 'uTime')
  // gl.uniform1f(program.uTime, 0.001 * performance.now())

  program.uResolution = gl.getUniformLocation(program, 'uResolution')
  gl.uniform2fv(program.uResolution, [window.innerWidth,window.innerHeight])

  program.uSeed = gl.getUniformLocation(program, 'uSeed')
  gl.uniform1f(program.uSeed, seed)

  program.uPoly = gl.getUniformLocation(program, 'uPoly')
  gl.uniform1f(program.uPoly, poly)

  program.uOpacity = gl.getUniformLocation(program, 'uOpacity')
  gl.uniform1f(program.uOpacity, opacity)

  program.uRotation = gl.getUniformLocation(program, 'uRotation')
  gl.uniform1f(program.uRotation, rotation)

  program.uPosition = gl.getUniformLocation(program, 'uPosition')
  gl.uniform1f(program.uPosition, position)

  program.uCol1 = gl.getUniformLocation(program, 'uCol1')
  gl.uniform3fv(program.uCol1, hexToRgb(color[0]))

  program.uCol2 = gl.getUniformLocation(program, 'uCol2')
  gl.uniform3fv(program.uCol2, hexToRgb(color[1]))

  program.uCol3 = gl.getUniformLocation(program, 'uCol3')
  gl.uniform3fv(program.uCol3, hexToRgb(color[2]))

  program.uCol4 = gl.getUniformLocation(program, 'uCol4')
  gl.uniform3fv(program.uCol4, hexToRgb(color[2]))

  program.uCol5 = gl.getUniformLocation(program, 'uCol5')
  gl.uniform3fv(program.uCol5, hexToRgb(color[3]))


  gl.drawArrays(gl.TRIANGLES, 0, numItems)

  requestAnimationFrame(render)
}

