var fragmentShaderSource = `
#ifdef GL_ES
precision highp float;
#endif

#extension GL_OES_standard_derivatives : enable

uniform vec2 uResolution;

uniform float uSeed;
uniform float uPoly;
uniform float uOpacity;
uniform float uPosition;
uniform float uRotation;
uniform vec3 uCol1;
uniform vec3 uCol2;
uniform vec3 uCol3;
uniform vec3 uCol4;
uniform vec3 uCol5;

#define PI 3.141593
#define TAU 6.28318

// patch uniforms
#define FREQ 50.

float random(float x) {
  return fract(sin(x) * 43758.5453);
}

float random(vec2 st) {
  return fract(sin(dot(st.xy, vec2(12.9898, 78.233))) * 43758.5453);
}

vec2 random2(vec2 st) {
    const vec2 k = vec2(.3183099, .3678794);
    st = st * k + k.yx;
    return -1. + 2. * fract(16. * k * fract(st.x * st.y * (st.x + st.y)));
}

vec3 random3(vec2 p) {
    vec3 p3 = fract(vec3(p.xyx) * vec3(.1031, .1030, .0973));
    p3 += dot(p3, p3.yxz+19.19);
    return fract((p3.xxy+p3.yzz)*p3.zyx);
}

vec3 random3(vec3 p) {
    p = vec3( dot(p, vec3(127.1, 311.7, 74.7)),
            dot(p, vec3(269.5, 183.3, 246.1)),
            dot(p, vec3(113.5, 271.9, 124.6)));
    return -1. + 2. * fract(sin(p) * 43758.5453123);
}

float ss(float s, float r) {
    float w = fwidth(s);
    return smoothstep(r+w,r-w,s);
}

float square(vec2 uv) {
    uv = abs(uv);
    return max(uv.y,uv.x);
}

float circle(vec2 uv) {
    return length(uv);
}

// https://iquilezles.org/www/articles/distfunctions2d/distfunctions2d.htm
float triangle(vec2 p)
{
    const float k = sqrt(3.0);
    p.x = abs(p.x) - 1.0;
    p.y = p.y + 1.0/k;
    if( p.x+k*p.y>0.0 ) p = vec2(p.x-k*p.y,-k*p.x-p.y)/2.0;
    p.x -= clamp( p.x, -2.0, 0.0 );
    return -length(p)*sign(p.y);
}

float shape(vec2 uv, vec3 id, float sh) {
    float s = abs(sin(random3(id).x*FREQ*sh));
    return s;
}

mat2 rot(float a) {
    return mat2(cos(a),sin(a),-sin(a),cos(a));
}

void main() {
    vec2 uv = (2.*gl_FragCoord.xy-uResolution)/uResolution.y;
    uv *= 6.;

    vec2 guv = fract(uv)-.5;
    vec2 id = floor(uv);

    if (uRotation == 1.) {
        guv *= rot(length(id));
    }

    if (uPosition == 1.) {
        guv += vec2(random(id),random(id));
    }
    
    float rand,sh;

    if (uPoly == 1.) {
        rand = random3(vec3(id,uSeed)).x*3.;
        sh = rand < 1. ? circle(guv) : rand < 2. ? square(guv) : triangle(guv);
    } else {
        rand = uSeed;
        sh = rand < .3 ? circle(guv) : rand < .6 ? square(guv) : triangle(guv);
    }
    
    vec3 col = clamp(vec3(shape(guv,random3(vec3(id,uSeed)),sh)),0.,1.);

    if (uOpacity == 1.) {
        col *= clamp(random3(vec3(id,uSeed)).rrr,0.,1.);
    }

    col *= clamp(random(uv),0.,1.);
    col *= mix(col,uCol1,random(id));
    
    gl_FragColor = vec4(col,1.);
}`

